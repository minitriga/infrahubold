export function getRandomInt(min: number, max: number) {
  min = Math.ceil(min);
  max = Math.floor(max);
  return Math.floor(Math.random() * (max - min + 1)) + min;
}

export const getRandomUser = () => {
  const randomIndex = getRandomInt(0, SAMPLE_USERS.length - 1);
  const user = SAMPLE_USERS[randomIndex];
  SAMPLE_USERS.splice(randomIndex, 1);
  return user;
};
